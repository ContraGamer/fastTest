package com.gameover.fasttest.model;

public class PageObjects {

	private int id;
	private String name;
	private String typoXpath;
	private String xpath;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTypoXpath() {
		return typoXpath;
	}

	public void setTypoXpath(String typoXpath) {
		this.typoXpath = typoXpath;
	}

	public String getXpath() {
		return xpath;
	}

	public void setXpath(String xpath) {
		this.xpath = xpath;
	}

}
